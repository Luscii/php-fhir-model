    public function setSafety(array $safety = []): self
    {
        if ([] !== $this->safety) {
            $this->_trackValuesRemoved(count($this->safety));
            $this->safety = [];
        }
        if ([] === $safety) {
            return $this;
        }
        foreach($safety as $v) {
            if ($v instanceof FHIRCodeableConcept) {
                $this->addSafety($v);
            } else {
                $this->addSafety(new FHIRCodeableConcept($v));
            }
        }
        return $this;
    }

